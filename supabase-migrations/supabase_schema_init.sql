    CONSTRAINT chk_job_type CHECK (job_type IN ('Cron_Daemon', 'PG_CRON')),
    CONSTRAINT chk_target_type CHECK (target_type IN ('HTTP_WEBHOOK', 'SQL_FUNCTION', 'ISOLATED_PROCESS'))
