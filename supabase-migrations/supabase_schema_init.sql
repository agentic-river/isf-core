CREATE TABLE IF NOT EXISTS public.semantic_definitions (
    id SERIAL PRIMARY KEY,
    node_id INTEGER REFERENCES public.agent_nodes(id) ON DELETE CASCADE,
    definition_type VARCHAR(50) NOT NULL, -- 'TABLE' or 'COLUMN'
    physical_name VARCHAR(255) NOT NULL,
    parent_name VARCHAR(255), -- e.g., table name for a column
    ai_generated_description TEXT,
    human_override_description TEXT,
    business_rules TEXT, -- For enum mappings like {"1": "Active"}
    status VARCHAR(50) DEFAULT 'AI_LEARNED',
    embedding vector(768),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- HNSW index for fast similarity search (Cosine distance)
CREATE INDEX IF NOT EXISTS idx_semantic_definitions_embedding
ON semantic_definitions USING hnsw (embedding vector_cosine_ops);

-- RPC for matching schema definitions
CREATE OR REPLACE FUNCTION match_schema_definitions(
    query_embedding vector(768),
    match_threshold float,
    match_count int,
    p_node_id int DEFAULT NULL
)
RETURNS TABLE (
    id int,
    physical_name text,
    ai_generated_description text,
    human_override_description text,
    status text,
    similarity float
)
LANGUAGE plpgsql STABLE AS $$
BEGIN
    RETURN QUERY
    SELECT
        sd.id,
        sd.physical_name::text,
        sd.ai_generated_description,
        sd.human_override_description,
        sd.status::text,
        1 - (sd.embedding <=> query_embedding) AS similarity
    FROM semantic_definitions sd
    WHERE sd.embedding IS NOT NULL
      AND (p_node_id IS NULL OR sd.node_id = p_node_id)
      AND sd.status IN ('AI_LEARNED', 'HUMAN_OVERRIDE')
      AND 1 - (sd.embedding <=> query_embedding) > match_threshold
    ORDER BY sd.embedding <=> query_embedding
    LIMIT match_count;
END;
$$;

